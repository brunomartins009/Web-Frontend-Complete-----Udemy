/* Operadores aritméticos
* Somar: (+)
* Subtrair: (-)
* Multiplicar: (*)
* Dividir: (/)
*/

let numero1 = 10;
let numero2 = 5;

let resultadoSoma = numero1 + numero2;
let resultadoSubtracao = numero1 - numero2;
let resultadoMultiplicacao = numero1 * numero2;
let resultadoDivisao = numero1 / numero2;

console.log("Soma: " + resultadoSoma);
console.log("Subtração: " + resultadoSubtracao);
console.log("Multiplicação: " + resultadoMultiplicacao);
console.log("Divisão: " + resultadoDivisao);