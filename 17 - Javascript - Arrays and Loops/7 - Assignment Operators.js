let salario = 500;
let bonus = 200;

salario = salario + bonus;
// Podemos usar o operador de atribuição composto +=
salario += bonus; // salario = salario + bonus

let desconto = 100;
salario = salario - desconto;
// Podemos usar o operador de atribuição composto -=
salario -= desconto; // salario = salario - desconto

let mult = 2;
salario = salario * mult;
// Podemos usar o operador de atribuição composto *=
salario *= mult; // salario = salario * mult

let div = 2;
salario = salario / div;
// Podemos usar o operador de atribuição composto /=
salario /= div; // salario = salario / div